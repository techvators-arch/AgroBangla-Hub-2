function findAnswer(question: string): string {
  const q = question.toLowerCase();
  let best: { score: number; answer: string } = { score: 0, answer: "" };

  for (const entry of KB) {
    let score = 0;
    for (const key of entry.keys) {
      if (q.includes(key.toLowerCase())) score += 2;
      // Partial match
      for (const word of q.split(/\s+/)) {
        if (key.includes(word) && word.length > 1) score += 0.5;
      }
    }
    if (score > best.score) best = { score, answer: entry.answer };
  }

  if (best.score > 0) return best.answer;

  // Fallback: default welcome
  return KB[KB.length - 1].answer;
}

// POST /api/ai-chat — SSE streaming response
router.post("/ai-chat", (req: Request, res: Response) => {
  const { message } = req.body as { message?: string };

  if (!message || typeof message !== "string" || !message.trim()) {
    res.status(400).json({ error: "message is required" });
    return;
  }

  res.setHeader("Content-Type", "text/event-stream");
  res.setHeader("Cache-Control", "no-cache");
  res.setHeader("Connection", "keep-alive");
  res.setHeader("Access-Control-Allow-Origin", "*");

  const answer = findAnswer(message.trim());

  // Stream word by word with realistic typing delays
  const words = answer.split(" ");
  let i = 0;

  const send = () => {
    if (i >= words.length) {
      res.write(`data: ${JSON.stringify({ done: true })}\n\n`);
      res.end();
      return;
    }

    const chunk = (i === 0 ? "" : " ") + words[i];
    res.write(`data: ${JSON.stringify({ content: chunk })}\n\n`);
    i++;

    // Variable speed — punctuation = longer pause
    const last = words[i - 1] ?? "";
    const delay = /[।।,\n]/.test(last) ? 80 : /[?!।]/.test(last) ? 120 : 28;
    setTimeout(send, delay);
  };

  // Small initial thinking delay
  setTimeout(send, 400);
});

